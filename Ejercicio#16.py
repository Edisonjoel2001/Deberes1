print("Programa que suma numeros del 1 al 10. ")

# Aqui inicializamos con la variable suma
suma = 0

# Usamos el bucle for para sumar numeros del 1 al 10
for i in range(1, 11):
    suma += i  # Sumar el valor de i a la variable suma

# Mostramos el resultado de la variable
print(f"La suma de los números del 1 al 10 es: {suma}")
